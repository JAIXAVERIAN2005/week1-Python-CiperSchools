name = "JaI yAdAv"
# 1.) len() function:-
print(len(name)) # it gives --> 9

# 2.) lower() method:-(Makes every letter small)
print(name.lower()) # it gives --> jai yadav

# 3.) upper() method:-(Makes every letter capital)
print(name.upper()) # it gives --> JAI YADAV

# 4.) title() method:-(Makes first letter capital)
print(name.title()) # it gives --> Jai Yadav

# 5.) count() method:-(Counts a specific letter)
print(name.count("y")) # it gives -->1